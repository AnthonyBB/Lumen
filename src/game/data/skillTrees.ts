export type SkillClass =
  | 'fire_mage' | 'ice_mage' | 'lightning_mage'
  | 'sword' | 'spear' | 'axe' | 'hammer'
  | 'monk' | 'paladin' | 'assassin' | 'cleric' | 'shaman' | 'bard'

export type EffectType =
  | 'damage' | 'heal' | 'dot' | 'debuff' | 'buff'
  | 'aoe' | 'stun' | 'lifesteal' | 'shield' | 'execute' | 'chain' | 'summon'

export interface SkillEffect {
  type: EffectType
  value: number
  duration?: number   // turns (dot/debuff/buff/stun/shield)
  chance?: number     // 0–1 probability
}

export interface CombatSkill {
  id: string
  name: string
  icon: string        // single emoji
  description: string
  tier: 1 | 2 | 3 | 4 | 5
  path: 'core' | 'path_a' | 'path_b'
  requires: string[]  // prerequisite skill ids
  effects: SkillEffect[]
  mpCost: number
  cooldown: number    // turns (0 = no cooldown)
  class: SkillClass
}

export interface SkillTreeDef {
  class: SkillClass
  label: string
  icon: string
  description: string
  skills: CombatSkill[]
}

